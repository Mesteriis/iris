    assert {detector.slug for detector in detectors} == {
        "head_shoulders",
        "inverse_head_shoulders",
        "double_top",
        "double_bottom",
        "triple_top",
        "triple_bottom",
        "ascending_triangle",
        "descending_triangle",
        "symmetrical_triangle",
        "rising_wedge",
        "falling_wedge",
        "rectangle_top",
        "rectangle_bottom",
        "broadening_top",
        "broadening_bottom",
        "expanding_triangle",
        "descending_channel_breakout",
        "ascending_channel_breakdown",
        "rounded_bottom",
        "rounded_top",
        "diamond_bottom",
        "diamond_top",
        "flat_base",
    }

    for detector in detectors:
        assert detector.detect(short, {}) == []


