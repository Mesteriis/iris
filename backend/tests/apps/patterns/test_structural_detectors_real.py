def _candles_from_ohlc(
    *,
    closes: list[float],
    highs: list[float] | None = None,
    lows: list[float] | None = None,
) -> list:
    base = datetime(2026, 3, 12, 12, 0, tzinfo=UTC)
    high_values = highs or [value + 1.0 for value in closes]
    low_values = lows or [value - 1.0 for value in closes]
    candles = []
    previous_close = closes[0]
    for index, close in enumerate(closes):
        candles.append(
            CandlePointFactory.build(
                timestamp=base + timedelta(minutes=15 * index),
                open=previous_close if index else close,
                high=high_values[index],
                low=low_values[index],
                close=close,
                volume=1000.0,
            )
        )
        previous_close = close
    return candles


def _assert_detected(detector, candles: list, expected_slug: str) -> None:
    detections = detector.detect(candles, {})
    assert detections and detections[0].slug == expected_slug


def test_structural_detectors_short_inputs_return_empty() -> None:
    short = build_candle_points(closes=[100.0 + index for index in range(12)], volumes=[1000.0] * 12)

